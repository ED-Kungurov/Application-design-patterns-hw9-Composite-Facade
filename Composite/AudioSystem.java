public class AudioSystem {
    void powerOn(){
        System.out.println("audio system powered on ");
    }
    void powerOff(){
        System.out.println("audio system powered off ");
    }
    void playLast(){
        System.out.println("audio system playing last track ");
    }
    void playNext(){
        System.out.println("audio system playing next track ");
    }
    void playPrev(){
        System.out.println("audio system playing previous track ");
    }
    void setLastVolume(){
        System.out.println("audio system set last volume ");
    }
    void setVolume(int volume){
        System.out.println("audio system set volume " + volume);
    }
}

